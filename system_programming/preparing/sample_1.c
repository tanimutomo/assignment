#include <stdio.h>

int main(void) {
    // String
    char var;
    var = 'A';
    printf("var is %c\n", var);

    // Integer
    int a, b, c;
    a = 22;
    printf("a is %d\n", a);

    // float
    float x;
    float y;
    float z;
    x = 33.33;
    printf("x is %f\n", x);

    // double
    double sum;
    sum = 44.444444444;
    printf("sum is %f\n", sum);

    return 0;
}

